function clm_function_is_uuid($uuid)
{
	if( !(bool)preg_match('/^[0-9a-fA-F]{8}\b-[0-9a-fA-F]{4}\b-[0-9a-fA-F]{4}\b-[0-9a-fA-F]{4}\b-[0-9a-fA-F]{12}$/', trim($uuid)) ) {
		return false; //echo "UUID ungültig!\n"; 
	}
    return true;
}
