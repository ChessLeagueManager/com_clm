// Ersatz fÃ¼r die ehemalige php-Funktion utf8_decode (deprecated in php 8.2)
// nach https://php.watch/versions/8.2/utf8_encode-utf8_decode-deprecated
// Verwendung: $string = clm_core::$load->utf8decode($string);

function clm_function_utf8decode(string $string): string {
    $s = (string) $string;
    $len = \strlen($s);

    for ($i = 0, $j = 0; $i < $len; ++$i, ++$j) {
        switch ($s[$i] & "\xF0") {
            case "\xC0":
            case "\xD0":
                $c = (\ord($s[$i] & "\x1F") << 6) | \ord($s[++$i] & "\x3F");
                $s[$j] = $c < 256 ? \chr($c) : '?';
                break;

            case "\xF0":
                ++$i;
                // no break

            case "\xE0":
                $s[$j] = '?';
                $i += 2;
                break;

            default:
                $s[$j] = $s[$i];
        }
    }

    return substr($s, 0, $j);
}
